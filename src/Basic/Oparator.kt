package Basic

fun main(args: Array<String>) {
    var a = 20
    var b = 5

    var num = 20.56
    var num1 = 50.50

    //Arithmetic Operator
    var sum = a+b //addition
    var name = "Ahosan" + "Chowdhury"
    println("$sum, $name")

    //Subtraction Operator
    var sub = a-b
    println(sub)

    //Multiplication Operator
    var multi = a*b
    println(multi)

    //Divider Operator
    var divid = a/b
    println(divid)

    var div = num/num1
    println(div)

    //Modules Operator
    var mod = num%num1
    println(mod)
}